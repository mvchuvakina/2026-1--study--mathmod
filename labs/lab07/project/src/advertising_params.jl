# Только платная реклама (α₂ = 0)
function only_paid!(du, u, p, t)
    n = u[1]
    α₁ = 0.68
    α₂ = 0.0
    du[1] = (α₁ + α₂ * n) * (N - n)
    nothing
end

# Только сарафанное радио (α₁ = 0)
function only_word!(du, u, p, t)
    n = u[1]
    α₁ = 0.0
    α₂ = 0.28
    du[1] = (α₁ + α₂ * n) * (N - n)
    nothing
end

# Экспорт новых функций
export only_paid!, only_word!
