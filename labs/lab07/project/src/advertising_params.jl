# src/advertising_params.jl
# Параметры для варианта №56

module AdvertisingParams

export N, n0, tspan, case1!, case2!, case3!

# Константы варианта
const N = 1505.0      # общее число потенциальных клиентов
const n0 = 7.0        # начальное число знающих
const tspan = (0.0, 30.0)  # временной интервал

# -------------------------------------------------------------------
# СЛУЧАЙ 1: α₁ = 0.68, α₂ = 0.00009 (постоянные)
# -------------------------------------------------------------------
function case1!(du, u, p, t)
    n = u[1]
    α₁ = 0.68
    α₂ = 0.00009
    du[1] = (α₁ + α₂ * n) * (N - n)
    nothing
end

# -------------------------------------------------------------------
# СЛУЧАЙ 2: α₁ = 0.00001, α₂ = 0.28 (постоянные)
# -------------------------------------------------------------------
function case2!(du, u, p, t)
    n = u[1]
    α₁ = 0.00001
    α₂ = 0.28
    du[1] = (α₁ + α₂ * n) * (N - n)
    nothing
end

# -------------------------------------------------------------------
# СЛУЧАЙ 3: α₁(t) = 0.1·sin(5t), α₂(t) = 0.4·cos(3t) (периодические)
# -------------------------------------------------------------------
function case3!(du, u, p, t)
    n = u[1]
    α₁ = 0.1 * sin(5*t)
    α₂ = 0.4 * cos(3*t)
    du[1] = (α₁ + α₂ * n) * (N - n)
    nothing
end

end
